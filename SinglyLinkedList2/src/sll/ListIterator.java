package sll;

/**
 * @author anderson
 * 
 * Same as java.util.ListIterator
 * @param <T> The type of the elements in the Collection.
 *
 */
public interface ListIterator<T> extends Iterator<T> {
	
	 /**
	  * @return true if this list iterator has more elements 
	  *  when traversing the list in the reverse direction.
	  */
	  boolean hasPrevious();
	  
	  /**
	   * @return the index of the element that would be 
	   * returned by a subsequent call to next.
	   */
	  int nextIndex();
	  
	  /**
	   * @return the previous element in the list.
	   */
	  T previous();
	  
	  /**
	   * 
	   * @return the index of the element that would be returned by a subsequent 
	   * call to previous.
	   */
	  boolean previousIndex();
	  
	  /**
	   * Replaces in the list the last element returned by by next or previous 
	   * with the specified element x (optional operation).
	   * @param x
	   */
	  void set(T x);
	  
	  
	 

}
